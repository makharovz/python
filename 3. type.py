# Функция type позволяет узнать, к какому типу относится объект. Например:

var_1 = 1                                                                       # целое число (int)
var_2 = 1.5                                                                     # число с плавающей точкой (float)
var_3 = 'Hello, World!'                                                         # строка (str)
var_4 = False                                                                   # истина/ложь (bool)
var_5 = [1, 2, 3, 4]                                                            # список (list)
var_6 = {'Hello', 'World', 'Devices', 'Cars'}                                   # множества (list)
var_7 = {'Name':'Zaur', 'Job Title':'QA Engineer', 'Company':'AFLT-Systems'}    # множества/ключ:значение (dict)

print(type(var_7))
